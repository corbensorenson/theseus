def render_statement_skeleton_body(tokens: list[str]) -> tuple[str, dict[str, Any]]:
    skeleton = []
    for tok in tokens:
        if tok == "<eos>":
            break
        if tok.startswith("SKEL:"):
            skeleton.append(tok)
    shape = decoded_return_shape(skeleton)
    has_loop = any(tok in {"SKEL:LOOP_DATA", "SKEL:LOOP_OTHER", "SKEL:LOOP_WHILE"} for tok in skeleton)
    has_branch = "SKEL:BRANCH_GUARD" in skeleton
    has_try = "SKEL:TRY_EXCEPT" in skeleton
    has_append = "SKEL:CALL_APPEND" in skeleton
    lines: list[str] = []
    if shape:
        lines.extend(initializer_for_shape(shape))
    elif has_loop:
        lines.append("result = []")
    elif any(tok.startswith("SKEL:ASSIGN_") for tok in skeleton):
        lines.append("result = None")
    if has_loop:
        lines.append("for item in data:")
        loop_indent = "    "
        if has_branch:
            lines.append(f"{loop_indent}if item is None:")
            lines.append(f"{loop_indent}    continue")
        if has_try:
            lines.append(f"{loop_indent}try:")
            for line in update_lines_for_shape(shape or "unknown", has_append=has_append):
                lines.append(f"{loop_indent}    {line}")
            lines.append(f"{loop_indent}except Exception:")
            lines.append(f"{loop_indent}    continue")
        else:
            for line in update_lines_for_shape(shape or "unknown", has_append=has_append):
                lines.append(f"{loop_indent}{line}")
    elif has_branch:
        lines.append("if data:")
        lines.append("    result = data")
    if shape:
        lines.append(return_line_for_shape(shape))
    body = "\n".join(lines).strip()
    return body, {
        "rendered_from_statement_skeleton": True,
        "skeleton_token_count": len(skeleton),
        "unique_skeleton_tokens": sorted(set(skeleton)),
        "predicted_return_shape": shape or "",
        "predicted_loop": has_loop,
        "predicted_branch": has_branch,
        "predicted_try": has_try,
        "predicted_append": has_append,
        "rendered_line_count": len(lines),
    }


def decoded_return_shape(skeleton: list[str]) -> str:
    for tok in skeleton:
        if tok.startswith("SKEL:RETURN_"):
            shape = tok.removeprefix("SKEL:RETURN_").lower()
            if shape in {"list", "dict", "set", "tuple", "number", "bool", "str", "none", "unknown"}:
                return shape
    return ""


def initializer_for_shape(shape: str) -> list[str]:
    if shape == "dict":
        return ["result = {}"]
    if shape == "set":
        return ["result = set()"]
    if shape == "tuple":
        return ["result = []"]
    if shape == "number":
        return ["result = 0"]
    if shape == "bool":
        return ["result = False"]
    if shape == "str":
        return ["result = ''"]
    if shape == "none":
        return []
    return ["result = []"]


def update_lines_for_shape(shape: str, *, has_append: bool) -> list[str]:
    if shape == "dict":
        return ["result[str(item)] = item"]
    if shape == "set":
        return ["result.add(item)"]
    if shape == "tuple":
        return ["result.append(item)"]
    if shape == "number":
        return ["result += int(item)"]
    if shape == "bool":
        return ["if item:", "    result = True"]
    if shape == "str":
        return ["result += str(item)"]
    if has_append or shape in {"list", "unknown", ""}:
        return ["result.append(item)"]
    return ["result = item"]


def return_line_for_shape(shape: str) -> str:
    if shape == "tuple":
        return "return tuple(result)"
    if shape == "bool":
        return "return bool(result)"
    if shape == "none":
        return "return None"
    if shape == "unknown":
        return "return result"
    return "return result"


def normalize_body_text(body: str) -> str:
    body = textwrap.dedent(str(body or ""))
    lines = []
    for raw in body.replace("\r\n", "\n").replace("\r", "\n").splitlines():
        line = raw.replace("\t", "    ").rstrip()
        if line.strip():
            lines.append(line)
    return "\n".join(lines).strip()


def token_values_to_line(values: list[str]) -> str:
    out = ""
    prev = ""
    binary_ops = {
        "=",
        "+",
        "-",
        "*",
        "/",
        "//",
        "%",
        "**",
        "==",
        "!=",
        "<",
        "<=",
        ">",
        ">=",
        "and",
        "or",
        "in",
        "is",
        "not",
    }
    no_space_before = {")", "]", "}", ",", ":", ".", ";"}
    no_space_after = {"(", "[", "{", ".", "@"}
    for raw in values:
        value = str(raw or "")
        if not value:
            continue
        if not out:
            out = value
        elif value in no_space_before:
            out += value
        elif prev in no_space_after:
            out += value
        elif value == "(" and (prev.isidentifier() or prev in {")", "]", "}"}):
            out += value
        elif value in binary_ops or prev in binary_ops:
            out += " " + value
        else:
            out += " " + value
        prev = value
    return out
