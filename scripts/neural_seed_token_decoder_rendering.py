def semantic_body_shortest_hops() -> str:
    return body_from_lines([
        "from collections import deque",
        "start = extra[0] if len(extra) > 0 else 0",
        "goal = extra[1] if len(extra) > 1 else 0",
        "graph = [[] for _ in range(max(0, int(data)))]",
        "for edge in other or []:",
        "    if not isinstance(edge, (list, tuple)) or len(edge) < 2:",
        "        continue",
        "    try:",
        "        a, b = int(edge[0]), int(edge[1])",
        "    except Exception:",
        "        continue",
        "    if 0 <= a < len(graph) and 0 <= b < len(graph):",
        "        graph[a].append(b)",
        "        graph[b].append(a)",
        "if start < 0 or goal < 0 or start >= len(graph) or goal >= len(graph):",
        "    return -1",
        "queue = deque([(start, 0)])",
        "seen = {start}",
        "while queue:",
        "    node, dist = queue.popleft()",
        "    if node == goal:",
        "        return dist",
        "    for nxt in graph[node]:",
        "        if nxt not in seen:",
        "            seen.add(nxt)",
        "            queue.append((nxt, dist + 1))",
        "return -1",
    ])


