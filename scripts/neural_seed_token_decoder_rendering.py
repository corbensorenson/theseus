"""Token-decoder rendering and deterministic body construction helpers.

This module owns body-token decoding, strict-action rendering, semantic-slot
rendering, and deterministic body renderer utilities for neural seed token
decoders. These renderers are diagnostic/tooling surfaces only; they do not
inspect tests/solutions, use public benchmark payloads, call a teacher, or grant
learned-code-generation credit.
"""

