PLAN_PREFIX_BODY_TARGET_MODE = "plan_prefix_body_tokens_v1"
PLAN_SEMANTIC_SLOTS_BODY_TARGET_MODE = "plan_semantic_slots_body_tokens_v1"
PLAN_SEMANTIC_STMT_SLOTS_BODY_TARGET_MODE = "plan_semantic_stmt_slots_body_tokens_v1"
PLAN_SEMANTIC_STMT_EXPR_TRANSITION_BODY_TARGET_MODE = "plan_semantic_stmt_expression_transition_body_tokens_v1"
PLAN_BODY_START_TOKEN = "SLOT:BODY_START"
BODY_EXPRESSION_TRACE_PREFIX = "TRACE:EXPR_"
TYPED_SEMANTIC_IR_TARGET_MODE = semantic_ir.TARGET_MODE


def task_family(task: dict[str, Any]) -> str:
    return str(
        get_path(task, ["decoder_contract", "type_family"], "")
        or task.get("targeted_private_residual_family_v3")
        or task.get("residual_concept")
        or "unknown"
    )


def learned_plan_prefix_target_mode(target_mode: str) -> bool:
    return str(target_mode or "") in {
        PLAN_PREFIX_BODY_TARGET_MODE,
        PLAN_SEMANTIC_SLOTS_BODY_TARGET_MODE,
        PLAN_SEMANTIC_STMT_SLOTS_BODY_TARGET_MODE,
        PLAN_SEMANTIC_STMT_EXPR_TRANSITION_BODY_TARGET_MODE,
    }


def learned_plan_semantic_slots_body_target_mode(target_mode: str) -> bool:
    return str(target_mode or "") in {
        PLAN_SEMANTIC_SLOTS_BODY_TARGET_MODE,
        PLAN_SEMANTIC_STMT_SLOTS_BODY_TARGET_MODE,
        PLAN_SEMANTIC_STMT_EXPR_TRANSITION_BODY_TARGET_MODE,
    }


