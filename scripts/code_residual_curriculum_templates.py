def inferred_required_constructs(template: dict[str, Any]) -> list[str]:
    text = f"{template.get('category') or ''} {template.get('prompt') or ''}".lower()
    required = ["branch", "locals"]
    if any(token in text for token in ["loop", "each", "items", "matrix", "fields", "positive", "nested", "window", "decode", "shift"]):
        required.insert(0, "loop")
    if any(token in text for token in ["string", "text", "decode", "identifier", "palindrome", "encoding"]):
        required.append("index_or_string_ops")
    if any(token in text for token in ["matrix", "nested", "list", "fields", "records"]):
        required.append("collection_ops")
    required.append("type_and_return_shape")
    return sorted(dict.fromkeys(required))


def skeleton_bias_for_template(template: dict[str, Any]) -> list[str]:
    text = f"{template.get('category') or ''} {template.get('prompt') or ''}".lower()
    bias: list[str] = []
    if "palindrome" in text:
        bias.extend(["string_normalization", "slice_reverse_compare", "literal_bool_return"])
    if "decode" in text and "shift" in text:
        bias.extend(["character_loop", "modular_shift", "string_join_return"])
    if "positive" in text and "items" in text:
        bias.extend(["list_type_guard", "positive_int_filter", "append_increment"])
    if "numeric_fields" in text or "signed integers" in text:
        bias.extend(["bytes_decode_guard", "regex_signed_int_scan", "list_return_builder"])
    if "matrix" in text and "border" in text:
        bias.extend(["rectangular_matrix_guard", "nested_index_loop", "numeric_accumulator"])
    if not bias:
        shape = str(template.get("return_shape") or inferred_return_shape(template))
        if shape in {"list", "dict", "tuple", "str", "bool", "number"}:
            bias.append(f"{shape}_return_builder")
        bias.append("branch_loop_local_state")
    return sorted(dict.fromkeys(bias))


def tests_for(entry: str, cases: list[tuple[Any, Any, Any]]) -> str:
    lines = []
    for left, right, expected in cases:
        if right is None:
            lines.append(f"assert {entry}({left!r}) == {expected!r}")
        else:
            lines.append(f"assert {entry}({left!r}, {right!r}) == {expected!r}")
    return "\n".join(lines) + "\n"
