def inferred_argument_roles(template: dict[str, Any]) -> dict[str, str]:
    declared = template.get("argument_roles")
    if isinstance(declared, dict) and declared:
        return {str(key): str(value) for key, value in declared.items()}
    cases = template.get("cases") or []
    has_other = any(len(case) >= 2 and case[1] is not None for case in cases)
    if has_other:
        return {"data": "primary_input", "other": "secondary_parameter"}
    return {"data": "primary_input"}


def inferred_return_shape(template: dict[str, Any]) -> str:
    cases = template.get("cases") or []
    for case in cases:
        if len(case) < 3:
            continue
        expected = case[2]
        if isinstance(expected, bool):
            return "bool"
        if isinstance(expected, dict):
            return "dict"
        if isinstance(expected, list):
            return "list"
        if isinstance(expected, tuple):
            return "tuple"
        if isinstance(expected, str):
            return "str"
        if isinstance(expected, (int, float)):
            return "number"
        if expected is None:
            return "none"
    return "unknown"


